(function() {
  const STORAGE_KEY = 'asa_lang';

  function getCurrentLang() {
    return localStorage.getItem(STORAGE_KEY) || 'id';
  }

  function setLang(lang) {
    localStorage.setItem(STORAGE_KEY, lang);
    applyLang(lang);
  }

  function applyLang(lang) {
    const dict = LANG[lang];
    if (!dict) return;

    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key]) {
        el.textContent = dict[key];
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key]) {
        el.placeholder = dict[key];
      }
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      if (dict[key]) {
        el.title = dict[key];
      }
    });

    // Update toggle buttons
    document.querySelectorAll('.lang-toggle-text').forEach(el => {
      el.textContent = dict.lang_switch;
    });

    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
      const flag = btn.querySelector('.lang-flag');
      if (flag) {
        flag.textContent = lang === 'id' ? 'EN' : 'ID';
      }
    });
  }

  function toggleLang() {
    const current = getCurrentLang();
    const next = current === 'id' ? 'en' : 'id';
    setLang(next);
  }

  // Expose globally
  window.ASA_i18n = { getCurrentLang, setLang, toggleLang, applyLang };

  // Apply on load
  document.addEventListener('DOMContentLoaded', function() {
    applyLang(getCurrentLang());
  });

  // Also apply immediately if DOM already loaded
  if (document.readyState !== 'loading') {
    applyLang(getCurrentLang());
  }
})();
